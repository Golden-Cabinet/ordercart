                <h2 class='dashboard-title pull-left'>Granule Formula Instructions</h2>
                <div class="alert"><strong>Please follow your practitioner's instructions.</strong> The information on this site is meant as general guidelines and your practitioner knows your situation better than we do.</div>

                <ol>
                    <li>Shake your bottle or bag of herbs thoroughly before each use. Add the prescribed number of scoops to a non-aluminum cup or glass.  Each scoop is equal to approximately 1.5 grams.</li>
                    <li>Pour enough boiling water over the herbs to fully dissolve them.</li>
                    <li>Wait 30-60 seconds and then stir vigorously to help fully dissolve the granules.
                        <div class="alert alert-info">Most of our granules are from a company called Legendary Herbs and their granule size is larger than many other brands. While the flavor and aromas of the herbs are better than smaller granules, it may take a little more work to get them to fully dissolve.</div>
                    </li>

                    <li>If you want to drink your formula immediately, you can add cold water as soon as the herbs are dissolved to bring it to the desired temperature.  Otherwise, just wait until your formula cools off enough to drink comfortably.</li>
                    <li>If the flavor of the formula is tolerable, it is beneficial to take your time drinking it—the flavors and aromas are an integral part of the medicine and will make the herbs even more effective.
                        <div class="alert alert-info">
                            <h5>If the flavor is truly intolerable, you have some options</h5>
                            <ul><li>The best option is to simply use less water and quickly drink your formula, followed by something that tastes better as a “chaser”.</li>
                                <li>Alternatively, you can skip the decoction and just mix the dry granules into applesauce or a similar food.</li>
                                <li>If this is still too much, you can always put your formula into empty gel-caps.  However, this is quite time consuming and you do not get the full benefit of the herb flavors.</li></ul>
                        </div>
                    </li>

                    <p>Please ask your practitioner before adding any sweetener to your formula!  All sweeteners have their own energetic properties and may or may not complement what your formula is doing.</p>
                    <p>We encourage you to try to embrace Chinese Medicine fully by experiencing the flavors and sensations involved in drinking your herbal decoction. It is amazing what your taste buds can tolerate when something is beneficial to your current health condition.</p>
                </ol>
