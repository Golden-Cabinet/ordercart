            <div class="tab-pane fade" id="moxa">
                <h2 class='dashboard-title pull-left'>Smokeless Moxa Instructions</h2>
                <ol>
                    <li>Before you light the moxa stick, check it for cracks. Large cracks in the stick can increase the chances of a piece of burning moxa breaking off and falling.
                        <div class="alert alert-info">Use caution if you find cracks.</div>
                    </li>
                    <li>Light one end of the stick over the flame of a gas stove or a candle. Rotate the stick in the flame to light it evenly all the way around.
                        <div class="alert alert-info">Moxa sticks are difficult to light with an ordinary lighter and the lighter will likely get too hot to touch.</div>
                    </li>
                    <li>The end of the moxa stick will glow red when it is lit and ready for use.</li>
                    <li>As you use the moxa, scrape all sides of the stick on an ashtray, jar, or other hard, non-flammable container to prevent ash from building up.  Eventually the stick will be shaped like the tip of a pencil.</li>
                    <li>Use the moxa stick as directed by your practitioner.
                        <div class="alert alert-info">If an area of your body lacks sensation (i.e. it’s numb to the touch) carefully monitor how hot the area is by feeling the heat level with your other hand.</div>
                    </li>
                    <li>To extinguish the moxa stick, fill a glass jar about half way with sand and submerge the lit end of the moxa in the sand until it is fully out. You can store the moxa in this jar of sand in between uses.</li>
                </ol>
                <p>If you have any questions or doubts, please contact your practitioner.</p>
